add = lambda x, y : x + y
sub = lambda x, y : x - y
mul = lambda x, y : x * y
div = lambda x, y : x / y

num1 = float(input('Enter first number  :'))
num2 = float(input('Enter second number :'))
print('Add :', add(num1, num2))
print('Sub :', sub(num1, num2))
print('Mul :', mul(num1, num2))
print('Div :', div(num1, num2))